package hw2.task2;

public class Car {

    String whoseCar;
    int hour;

    public Car(String whoseCar, int hour) {
        this.whoseCar = whoseCar;
        this.hour = hour;
    }

    public String getWhoseCar() {
        return whoseCar;
    }

    public int getHour() {
        return hour;
    }
}
